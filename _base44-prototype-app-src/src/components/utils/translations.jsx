// Default language
let currentLanguage = "en";

// Get and set language functions
export const getLanguage = () => currentLanguage;
export const setLanguage = (lang) => {
  currentLanguage = lang;
  localStorage.setItem("pringles-language", lang);
  window.dispatchEvent(new Event("language-changed"));
};

// Initialize language from localStorage if available
export const initLanguage = () => {
  const savedLang = localStorage.getItem("pringles-language");
  if (savedLang) {
    currentLanguage = savedLang;
  }
};

// Get text based on current language
export function getText(path) {
  const languages = { en, fi, de };
  const bundle = languages[currentLanguage] || en;

  const keys = path.split('.');
  let result = bundle;

  for (const key of keys) {
    if (result[key] === undefined) {
      console.warn(`Translation missing for key: ${path} in ${currentLanguage}`);
      return path;
    }
    result = result[key];
  }

  return result;
}